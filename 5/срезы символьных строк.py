a = '1234567890'
print(a)
b = a[:2]  # от начала строки до символа с индексом №2, не включая
print(b)
c = a[2:]  # от символа с индексом №2 до конца
print(c)
d = a[::-1]  # реверс
print(d)
e = a[2:5]  # диапазон индексов со 2 по 5, не включая 5
print(e)
e = a[0:3]  # диапазон индексов с 0 по 3, не включая 3
print(e)
# 1234567890 символы
# 0123456789 номера